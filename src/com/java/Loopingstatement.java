package com.java;

public class Loopingstatement {
	public static void main(String[] agrs) {
		int a=5;
		while(a>=0) {
			System.out.println(a);
			a--;
		}
		
		
		int b=5;
		do {
			System.out.println(" hello world");
			b--;
		}while(b>=0);
		
		for(int i=5;i>=0;i--) {
			System.out.println(i);
			
		}
		int c = 40;
		do{
		
		System.out.println("Welcome to do block"+c); 
		c++;
		}while(c>30);
	}

}
